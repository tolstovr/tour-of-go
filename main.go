package main

import "fmt"

var c, python, java bool // global variables

func main() {
	var i int
	fmt.Println(i, c, python, java) // by default, bools are false and ints are 0

	var aboba int = 50 // initializing a variable
	fmt.Println(aboba)

	bebra := "Short way to initialize a variable"
	fmt.Println(bebra)
}
